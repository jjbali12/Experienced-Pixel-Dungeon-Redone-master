public class SpyGlass extends Item {

    {
        image = ItemSpriteSheet.SPYGLASS;

        stackable = true;
    }

    @Override
    protected void onThrow(int cell) {

        Char ch = Actor.findChar(cell);

        if (ch != null) {
            GLog.p(Messages.get(this, "it_is", ch.properties(), ch.HT, ch.defenseSkill(ch), ch.damageRoll(), ch.attackSkill(ch), ch.pos, ch.alignment, ch.paralysed, ch.rooted, ch.flying, ch.invisible));
            GLog.p(Messages.get(this, "it_is2", ch.name(), ch.buffs()));
        }

        this.quantity(1);
    }

    @Override
    public boolean isUpgradable() {
        return false;
    }

    @Override
    public boolean isIdentified() {
        return true;
    }

    @Override
    public int value() {
        return 30 * quantity;
    }
}
