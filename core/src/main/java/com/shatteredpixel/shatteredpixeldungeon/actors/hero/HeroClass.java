	private static void initRK( Hero hero ) {
		MagesStaff staff;
		staff = new MagesStaff(new WandOfMagicMissile());

		(hero.belongings.weapon = staff).identify();
		hero.belongings.weapon.activate(hero);

		Dungeon.quickslot.setSlot(0, staff);

		if (hero.belongings.armor != null){
			hero.belongings.armor.affixSeal(new BrokenSeal());
		}

		SpiritBow bow = new SpiritBow();
		bow.identify().collect();

		Dungeon.quickslot.setSlot(1, bow);

		CloakOfShadows cloak = new CloakOfShadows();
		(hero.belongings.artifact = cloak).identify();
		hero.belongings.artifact.activate( hero );

		Dungeon.quickslot.setSlot(2, cloak);

		new ScrollHolder().collect();
		Dungeon.LimitedDrops.SCROLL_HOLDER.drop();
		new PotionBandolier().collect();
		Dungeon.LimitedDrops.POTION_BANDOLIER.drop();
		new MagicalHolster().collect();
		Dungeon.LimitedDrops.MAGICAL_HOLSTER.drop();
		new TreasureHolder().collect();
		Dungeon.LimitedDrops.TREASURE_HOLDER.drop();

		new PotionOfHealing().identify();
		new ScrollOfRage().identify();

		// TODO add more items to add in start of the game
		//OTHERS - LUCKYs
		TicketToArena ticket = new TicketToArena();
		ticket.quantity(1000).collect();

		ExpGenerator exp = new ExpGenerator();
		exp.quantity(1000).collect();

		PotionBag Pbag = new PotionBag();
		Pbag.quantity(1000).collect();

		ScrollBag Sbag = new ScrollBag();
		Sbag.quantity(1000).collect();

		MiscBag Mbag = new MiscBag();
		Mbag.quantity(1000).collect();

		Cheese ch = new Cheese();
		ch.quantity(1000).collect();

		EtherealChains eth = new EtherealChains();
		eth.collect();

		AlchemistsToolkit alc = new AlchemistsToolkit();
		alc.collect();

	}

	public String title() {
		return Messages.get(HeroClass.class, name());
	}

